package crawlers.reportgui;

public class HUDReport {
    private String reportID;
    private String datasetName;
    private String fiscalYear;
    private String stateName;
    private String stateCode;
    private String countyName;
    private String fipsCode;
    private String msaCode;
    private String areaType;
    private String amiMedianFamilyIncome;
    private String propertyId;
    private String propertyName;
    private String propertyAddress;
    private String ownerEntity;
    private String totalUnits;
    private String occupiedUnits;
    private String vacancyRate;
    private String hudInspectionScore;
    private String zipCode;
    private String assistedUnits;

    public HUDReport() {}

    public String getReportID() { return reportID; }
    public void setReportID(String reportID) { this.reportID = reportID; }

    public String getDatasetName() { return datasetName; }
    public void setDatasetName(String datasetName) { this.datasetName = datasetName; }

    public String getFiscalYear() { return fiscalYear; }
    public void setFiscalYear(String fiscalYear) { this.fiscalYear = fiscalYear; }

    public String getStateName() { return stateName; }
    public void setStateName(String stateName) { this.stateName = stateName; }

    public String getStateCode() { return stateCode; }
    public void setStateCode(String stateCode) { this.stateCode = stateCode; }

    public String getCountyName() { return countyName; }
    public void setCountyName(String countyName) { this.countyName = countyName; }

    public String getFipsCode() { return fipsCode; }
    public void setFipsCode(String fipsCode) { this.fipsCode = fipsCode; }

    public String getMsaCode() { return msaCode; }
    public void setMsaCode(String msaCode) { this.msaCode = msaCode; }

    public String getAreaType() { return areaType; }
    public void setAreaType(String areaType) { this.areaType = areaType; }

    public String getAmiMedianFamilyIncome() { return amiMedianFamilyIncome; }
    public void setAmiMedianFamilyIncome(String amiMedianFamilyIncome) { this.amiMedianFamilyIncome = amiMedianFamilyIncome; }

    public String getPropertyId() { return propertyId; }
    public void setPropertyId(String propertyId) { this.propertyId = propertyId; }

    public String getPropertyName() { return propertyName; }
    public void setPropertyName(String propertyName) { this.propertyName = propertyName; }

    public String getPropertyAddress() { return propertyAddress; }
    public void setPropertyAddress(String propertyAddress) { this.propertyAddress = propertyAddress; }

    public String getOwnerEntity() { return ownerEntity; }
    public void setOwnerEntity(String ownerEntity) { this.ownerEntity = ownerEntity; }

    public String getTotalUnits() { return totalUnits; }
    public void setTotalUnits(String totalUnits) { this.totalUnits = totalUnits; }

    public String getOccupiedUnits() { return occupiedUnits; }
    public void setOccupiedUnits(String occupiedUnits) { this.occupiedUnits = occupiedUnits; }

    public String getVacancyRate() { return vacancyRate; }
    public void setVacancyRate(String vacancyRate) { this.vacancyRate = vacancyRate; }

    public String getHudInspectionScore() { return hudInspectionScore; }
    public void setHudInspectionScore(String hudInspectionScore) { this.hudInspectionScore = hudInspectionScore; }

    public String getZipCode() { return zipCode; }
    public void setZipCode(String zipCode) { this.zipCode = zipCode; }

    public String getAssistedUnits() { return assistedUnits; }
    public void setAssistedUnits(String assistedUnits) { this.assistedUnits = assistedUnits; }
}
